    public void tampilData() {
    DefaultTableModel tbl = new DefaultTableModel();
    tbl.addColumn("No");
    tbl.addColumn("Nama");
    tbl.addColumn("Harga");
    tbl.addColumn("Desain");
    tbl.addColumn("Kualitas");
    tbl.addColumn("Kenyamanan");
    tbl.addColumn("Ukuran");
   
    try {
        Connection conn = koneksi.koneksiDB();
        String sql = "SELECT * FROM alternatif";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();
        int no = 1;
        while (rs.next()) {
            tbl.addRow(new Object[]{ 
                no++,
                rs.getString(2),
                rs.getString(3),
                rs.getString(4),
                rs.getString(5),
                rs.getString(6),
                rs.getString(7),
            });
        }

        tblAlternatif.setModel(tbl);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal menampilkan data: " + e.getMessage());
        e.printStackTrace();
    }
}

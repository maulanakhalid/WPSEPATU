    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
                // TODO add your handling code here:
          // Ambil nilai dari input fields dan combo box
    // Mengambil nilai dari input fields

String namaAlternatif = txtnama.getText();
String hargaAlternatif = txtharga.getText();
String desainAlternatif = txtdesain.getText();
String kualitasAlternatif = txtkualitas.getText();
String kenyamananAlternatif = txtkenyamanan.getText();
String ukuranAlternatif = txtukuran.getText();

// Pastikan nilai tidak kosong
if (!namaAlternatif.isEmpty() && !hargaAlternatif.isEmpty() && !desainAlternatif.isEmpty() && !kualitasAlternatif.isEmpty() && !kenyamananAlternatif.isEmpty()&& !ukuranAlternatif.isEmpty()) {
    // Koneksi ke database
    Connection kon = null;
    PreparedStatement pstmt = null;
    
    try {
        // Dapatkan koneksi dari kelas Koneksi
        kon = koneksi.koneksiDB();
        
        // Query untuk memasukkan data
        String sql = "INSERT INTO alternatif (nama, harga, desain, kualitas, kenyaman, ukuran) VALUES (?, ?, ?, ?, ?, ?)";
        pstmt = kon.prepareStatement(sql);
        pstmt.setString(1, namaAlternatif);
        pstmt.setString(2, hargaAlternatif);
        pstmt.setString(3, desainAlternatif);
        pstmt.setString(4, kualitasAlternatif);
        pstmt.setString(5, kenyamananAlternatif);
        pstmt.setString(6, ukuranAlternatif);
       
        // Eksekusi query
        pstmt.executeUpdate();
        
        // Tampilkan pesan sukses
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
        
        // Kosongkan input fields setelah data disimpan
        
        txtnama.setText("");
        txtharga.setText("");
        txtdesain.setText("");
        txtkualitas.setText("");
        txtkenyamanan.setText("");
        txtukuran.setText("");
        tblAlternatif();
    } catch (Exception e) {
        // Tampilkan pesan kesalahan jika terjadi kesalahan
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } 
} else {
    // Tampilkan pesan kesalahan jika ada nilai yang kosong
    JOptionPane.showMessageDialog(this, "Semua field harus diisi.", "Error", JOptionPane.ERROR_MESSAGE);
}



    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        // Ambil nilai dari input fields dan combo box
    String nama = txtnama.getText();
    String harga = txtharga.getText();
    String desain = txtdesain.getText();
    String kualitas = txtkualitas.getText();
    String kenyamanan = txtkenyamanan.getText();
    String ukuran = txtukuran.getText();

    // Dapatkan baris yang dipilih dari tabel
    int selectedRow = tblAlternatif.getSelectedRow();
    if (selectedRow != -1) {
        // Dapatkan id dari baris yang dipilih
        int id = Integer.parseInt(tblAlternatif.getValueAt(selectedRow, 0).toString());

        if (!nama.isEmpty() && !harga.isEmpty() && !desain.isEmpty() && !kualitas.isEmpty() && !kenyamanan.isEmpty() && !ukuran.isEmpty()) {
            try {
                // Update data di database
                Connection kon = koneksi.koneksiDB();
                String sql = "UPDATE alternatif SET nama = ?, harga = ?, desain = ?, kualitas = ?, kenyamanan = ?, ukuran = ? WHERE id = ?";
                PreparedStatement pst = kon.prepareStatement(sql);
                pst.setString(1, nama);
                pst.setString(2, harga);
                pst.setString(3, desain);
                pst.setString(4, kualitas);
                pst.setString(5, kenyamanan);
                pst.setString(6, ukuran);
                pst.setInt(7, id);
                pst.executeUpdate();

                // Perbarui tabel tampilan setelah data diupdate
                DefaultTableModel model = (DefaultTableModel) tblAlternatif.getModel();
                model.setValueAt(nama, selectedRow, 1);
                model.setValueAt(harga, selectedRow, 2);
                model.setValueAt(desain, selectedRow, 3);
                model.setValueAt(kualitas, selectedRow, 4);
                model.setValueAt(kenyamanan, selectedRow, 5);
                model.setValueAt(ukuran, selectedRow, 6);
                
                

//                 Kosongkan input fields setelah data disimpan
                txtnama.setText("");
                txtharga.setText("");
                txtdesain.setText("");
                txtkualitas.setText("");
                txtkenyamanan.setText("");
                txtukuran.setText("");
                
                tampilData();

                JOptionPane.showMessageDialog(this, "Data berhasil diupdate.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Harap isi semua field sebelum menyimpan data.", "Input Kosong", JOptionPane.WARNING_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(this, "Harap pilih data yang ingin diupdate dari tabel.", "Tidak Ada Data Terpilih", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_btneditActionPerformed

